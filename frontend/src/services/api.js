import axios from 'axios';

const API_URL = process.env.REACT_APP_API_URL || 'http://localhost:3001/api';

// Configurar axios com interceptors
const api = axios.create({
  baseURL: API_URL,
  timeout: 10000, // 10 segundos
  headers: {
    'Content-Type': 'application/json',
  },
});

// Interceptor para requests
api.interceptors.request.use(
  (config) => {
    console.log(`üöÄ API Request: ${config.method?.toUpperCase()} ${config.url}`);
    return config;
  },
  (error) => {
    console.error('‚ùå Request Error:', error);
    return Promise.reject(error);
  }
);

// Interceptor para responses
api.interceptors.response.use(
  (response) => {
    console.log(`‚úÖ API Response: ${response.status} ${response.config.url}`);
    return response;
  },
  (error) => {
    console.error('‚ùå Response Error:', error.response?.status, error.response?.data || error.message);
    
    // Tratamento espec√≠fico para diferentes tipos de erro
    if (error.code === 'ECONNREFUSED') {
      console.error('üîå Servidor backend n√£o est√° rodando na porta 3001');
    } else if (error.response?.status === 500) {
      console.error('üí• Erro interno do servidor');
    } else if (error.response?.status === 404) {
      console.error('üîç Recurso n√£o encontrado');
    }
    
    return Promise.reject(error);
  }
);

